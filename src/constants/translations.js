export const t = (translations, language) => (key, params = {}) => {
    const keys = key.split('.');
    let value = translations[language];
    for (const k of keys) {
        value = value?.[k];
    }
    if (typeof value === 'string') {
        return value.replace(/\{\{(\w+)\}\}/g, (match, param) => params[param] || match);
    }
    return value || key;
};